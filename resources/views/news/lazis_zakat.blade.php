                        <p>Taipei - Alms of charity are interpreted as purification of oneself after fasting in the month of Ramadan. In addition, alms fitrah can also be interpreted as a form of caring for people who are less able, share the joy and victory on the holiday. For every Muslim soul, it is obligatory to fulfill zakat fitrah, the amount of which is equal to 2.5 kg of rice or staple food. As the hadith of Ibn Umar RA, "Rasulullah SAW requires zakat fitrah of one sha 'date or one sha' of wheat for Muslims; both slaves of light and free, male or female, small or large. He has ordered it to be carried out before the people come out for prayer. " (Bukhari Muslim)..</p>

                        <p>  Based on the consensus of the ulemas of the Nahdlatul Ulama Special Branch Management (PCI NU) of Taiwan, it was agreed that the amount of zakat fitrah to be issued would be $ 175 NTD with a rice price calculation of $ 70 NTD / kg and then rounded up to 200 NTD for each muzaki, where the excess would be infaq included. , while for fidyah is $ 50 NTD.

                        <p>The theme of Ramadhan that was carried out following the NU-Care Lazisnu Center this year was Ramadhan Gembira, in accordance with the theme, it was hoped that aid could be immediately distributed to the mustahik especially the victims affected by COVID19 in Indonesia. In a pandemic such as this, it causes obstacles in the pickup of zakat fitrah when meeting with muzaki, because the government expressly forbids people to gather on a large scale either indoor or open. Nevertheless, it did not reduce the spirit of the amil in inviting Indonesian citizens residing in Taiwan. 
                        </p>

                        <p>Initially the team from NU-Care LAZISNU Taiwan felt pessimistic about the acquisition of zakat fitrah this year. This is related to the impact of Corona and the Taiwan government's ban on avoiding the crowd. However, on the contrary, with a strategy to maximize social media as a socialization and mobilize teams from every branch in Taiwan, as well as optimize delivery through ATMs or Post Offices, the Lazisnu Taiwan NU-Care Team received 2,671 muzaki with the amount of zakat nature of Rp. 229,038,350, - fidyah of Rp. 2,548,000 and infaq of Rp. 47,409,950.
                        </p>

                        <p>"Thank God, the total distribution of zakat this year increased 23.9% from the previous year. Total unexpected, because of our limitations in this pandemic, but it turns out our intentions and prayers were facilitated by Allah SWT. " Said Nur Kholiq, Chairperson of Taiwan's Lazisnu NU-Care.
